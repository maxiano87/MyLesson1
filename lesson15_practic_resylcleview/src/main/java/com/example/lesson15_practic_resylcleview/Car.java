package com.example.lesson15_practic_resylcleview;

public class Car {
    private int image;
    private String description;

    public Car(int image, String description) {
        this.image = image;
        this.description = description;
    }

    public int getImage() {
        return image;
    }

    public String getDescription() {
        return description;
    }
}
